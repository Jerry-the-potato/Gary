  enableHighContrastMode,
  disableHighContrastMode,
  toggleHighContrastMode,

  // Reduced motion mode
  enableReducedMotion,
  disableReducedMotion,
  toggleReducedMotion,

  // Initialize
  initialize
} = useAccessibility()

// 組件狀態
const canvasElement = ref<HTMLCanvasElement | null>(null)
const dataInput = ref('64,34,25,12,22,11,90')

// 無障礙相關狀態
const showKeyboardHelp = ref(false)
const dataInputError = ref<string | null>(null)
const dataInputErrorId = createUniqueId('data-input-error')
const generateBtnDesc = createUniqueId('generate-btn-desc')
const resetBtnDesc = createUniqueId('reset-btn-desc')

// 從 stores 計算屬性
const selectedAlgorithm = computed(() => sortingStore.selectedAlgorithm)
const currentData = computed(() => sortingStore.currentData)
const playerState = computed(() => sortingStore.playerState)
const currentStep = computed(() => sortingStore.currentStep)
const totalSteps = computed(() => sortingStore.totalSteps)
